    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        // TODO add your handling code here:
        int selectedRow = Table.getSelectedRow();
        if (selectedRow != -1) {
            String kode_mk = Table.getValueAt(selectedRow, 0).toString();
            String sks = Table.getValueAt(selectedRow, 1).toString();
            String nama_mk = Table.getValueAt(selectedRow, 2).toString();
            String semester_ajar = Table.getValueAt(selectedRow, 3).toString();

            TFkode.setText(kode_mk);
            TFsks.setText(sks);
            TFnama.setText(nama_mk);
            TFsemester.setText(semester_ajar);
        }
    }//GEN-LAST:event_TableMouseClicked

