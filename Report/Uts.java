    private void BinsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BinsertActionPerformed
        // TODO add your handling code here:
        String KodeMK = TFkode.getText();
        String SKS = TFsks.getText();
        String NamaMK = TFnama.getText();
        String SemesterAjar = TFsemester.getText();
        if (KodeMK.trim().isEmpty() && SKS.trim().isEmpty() && NamaMK.trim().isEmpty() && SemesterAjar.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan data anda");

        } else if (KodeMK.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan kodeMK");
        } else if (SKS.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan Jumlah SKS");
        } else if (NamaMK.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan NamaMK");
        } else if (SemesterAjar.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan SemesterAjar");
        }
        else {
            PreparedStatement preparedStatement;
            try {
                String sql = "INSERT INTO matakuliah (kode_mk, sks, nama_mk, semester_ajar) VALUES (?, ?, ?, ?)";

                preparedStatement = conn.prepareStatement(sql);

                preparedStatement.setString(1, TFkode.getText());
                preparedStatement.setString(2, TFsks.getText());
                preparedStatement.setString(3, TFnama.getText());
                preparedStatement.setString(4, TFsemester.getText());

                preparedStatement.executeUpdate();
                this.getAllData();
                this.refreshModel();
                JOptionPane.showMessageDialog(this, "data berhasil ditambahkan");

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "data gagal ditambahkan", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BinsertActionPerformed

